getExperimentsResults = function(tasks) {

  aux = lapply(tasks$task.id, function(id) {
    res = listOMLRunEvaluations(task.id = id, limit = 10000)
    res$task.id = id
    return(res)
  })

  df = plyr::rbind.fill(aux)
  return(df)
}

